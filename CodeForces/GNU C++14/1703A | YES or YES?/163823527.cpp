#include<bits/stdc++.h>
using namespace std;
int main(){
    int t;
    cin>>t;
    while(t--){
        string s;
        cin>>s;
        
        if(s=="YES"||s=="yes"||s=="Yes"||s=="YEs"||s=="yEs"||s=="YeS"||s=="yeS"||s=="yES"){
            cout<<"YES"<<endl;
        }
        else{
            cout<<"NO"<<endl;
        }
    }
 
    return 0;
}
 